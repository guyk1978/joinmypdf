/**
 * For tools with a dedicated App Router page (tools/{slug}/page.tsx),
 * generate nested re-exports under each inventory hub:
 *   tools/{hub-segment}/{slug}/page.tsx → ../../{slug}/page
 *
 * Static segments beat hub [slug] catch-alls, fixing 404s for tools like
 * case-converter that are not rendered by tools/[slug]/page.tsx.
 *
 * Only re-exports symbols that exist on the dedicated page
 * (`generateMetadata`, `metadata`, and/or `default`) so typecheck passes.
 *
 * Run: node scripts/generate-hub-dedicated-tool-pages.cjs
 */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const toolsRoot = path.join(root, "src", "app", "[locale]", "tools");
const inventoryPath = path.join(root, "src", "data", "tools-inventory.ts");
const inventorySrc = fs.readFileSync(inventoryPath, "utf8");

const CATEGORY_HUB_SEGMENT = {
  pdf: "pdf-tools",
  video: "video-tools",
  mp4: "mp4-tools",
  convert: "convert-tools",
  compress: "compress-tools",
  extract: "extract-tools",
  image: "image-tools",
  jpg: "jpg-tools",
  png: "png-tools",
  mp3: "mp3-tools",
  audio: "mp3-tools",
  favicon: "favicon-tools",
  text: "text-tools",
  json: "json-tools",
  yaml: "yaml-tools",
  xml: "xml-tools",
  developer: "developer-tools",
  word: "word-tools",
  excel: "excel-tools",
  crop: "crop-tools",
  rotate: "rotate-tools",
  security: "security-tools",
  design: "developer-tools",
  data: "data-conversion-tools",
  productivity: "productivity-tools",
  "unit-math": "unit-converters",
  network: "network-tools",
};

/** Extra nests (same as sitemap overrides). */
const EXTRA_NESTS = {
  "compress-image": ["jpg"],
};

function parseInventory() {
  const tools = [];
  const blockRe =
    /\{\s*id:\s*"([^"]+)"[\s\S]*?categories:\s*\[([^\]]*)\][\s\S]*?primaryCategory:\s*"([^"]+)"/g;
  let match;
  while ((match = blockRe.exec(inventorySrc))) {
    const id = match[1];
    const categories = [...match[2].matchAll(/"([^"]+)"/g)].map((m) => m[1]);
    const primary = match[3];
    if (!categories.includes(primary)) categories.push(primary);
    for (const extra of EXTRA_NESTS[id] || []) {
      if (!categories.includes(extra)) categories.push(extra);
    }
    tools.push({ id, categories, primary });
  }
  return tools;
}

function hasDedicatedPage(slug) {
  return fs.existsSync(path.join(toolsRoot, slug, "page.tsx"));
}

/** Inspect which Next.js page exports the dedicated tool page provides. */
function inspectDedicatedExports(slug) {
  const src = fs.readFileSync(path.join(toolsRoot, slug, "page.tsx"), "utf8");
  const hasGenerateMetadata =
    /\bexport\s+(?:async\s+)?function\s+generateMetadata\b/.test(src) ||
    /\bexport\s*\{[^}]*\bgenerateMetadata\b/.test(src);
  const hasMetadata = /\bexport\s+const\s+metadata\b/.test(src);
  return { hasGenerateMetadata, hasMetadata };
}

function pageTemplate(slug, exports) {
  const names = ["default"];
  if (exports.hasGenerateMetadata) names.push("generateMetadata");
  else if (exports.hasMetadata) names.push("metadata");

  return `/**
 * Nested hub route for dedicated tool page.
 * Generated by scripts/generate-hub-dedicated-tool-pages.cjs
 */
export { ${names.join(", ")} } from "../../${slug}/page";
`;
}

const tools = parseInventory();
let wrote = 0;
let skipped = 0;

for (const tool of tools) {
  if (!hasDedicatedPage(tool.id)) {
    skipped += 1;
    continue;
  }

  const exports = inspectDedicatedExports(tool.id);
  const body = pageTemplate(tool.id, exports);

  for (const categoryId of tool.categories) {
    const hub = CATEGORY_HUB_SEGMENT[categoryId];
    if (!hub) continue;

    // Avoid clobbering existing special nests (e.g. network-tools/my-ip custom page)
    const targetDir = path.join(toolsRoot, hub, tool.id);
    const targetPage = path.join(targetDir, "page.tsx");
    const hubSlugCatchAll = path.join(toolsRoot, hub, "[slug]", "page.tsx");

    // If a custom nested page already exists and is not our generated re-export, keep it.
    if (fs.existsSync(targetPage)) {
      const existing = fs.readFileSync(targetPage, "utf8");
      if (
        !existing.includes(`from "../../${tool.id}/page"`) &&
        !existing.includes("generate-hub-dedicated-tool-pages")
      ) {
        console.log("keep custom", path.relative(root, targetPage));
        continue;
      }
    }

    fs.mkdirSync(targetDir, { recursive: true });
    fs.writeFileSync(targetPage, body);
    wrote += 1;
    console.log(
      "wrote",
      path.relative(root, targetPage),
      exports.hasGenerateMetadata
        ? "(default+generateMetadata)"
        : exports.hasMetadata
          ? "(default+metadata)"
          : "(default)",
    );

    // Ensure hub catch-all still exists for registry tools
    if (!fs.existsSync(hubSlugCatchAll)) {
      console.log("warn: missing hub catch-all", hub);
    }
  }
}

console.log(`done: wrote ${wrote} dedicated nest pages, skipped ${skipped} non-dedicated tools`);
