import { NextResponse } from 'next/server';
import { jsPDF } from 'jspdf';

export const runtime = 'edge';

const corsHeaders = {
  'Access-Control-Allow-Origin': 'https://wattquick.com',
  'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
  'Access-Control-Max-Age': '86400',
  'Access-Control-Allow-Headers': 'Content-Type, Authorization',
};

// פונקציית עזר להוספת Footer
const addFooter = (doc: any) => {
  const pageCount = doc.internal.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    doc.setFontSize(10);
    doc.setTextColor(128, 128, 128);
    doc.text(
      "Generated by WattQuick | Manage, Edit, and Convert PDFs at JoinMyPDF.com",
      doc.internal.pageSize.getWidth() / 2,
      doc.internal.pageSize.getHeight() - 10,
      { align: 'center' }
    );
    doc.link(50, doc.internal.pageSize.getHeight() - 15, 110, 10, { url: 'https://joinmypdf.com' });
  }
};

// פונקציית עזר לצבעים
const getStatusColor = (value: string | number) => {
  const val = String(value).toLowerCase();

  // 1. כחול לכל מה שקשור לכסף או עלות (סימן $)
  if (val.includes("$")) return [0, 102, 204];

  // 2. ירוק לכל מה שקשור לאחוזים (יעילות, אחוזים)
  if (val.includes("%")) return [40, 167, 69];

  // 3. כתום לכל מה שקשור למידות חשמליות (W, Wh, V, Ah)
  if (val.includes("w") || val.includes("v") || val.includes("ah")) return [255, 140, 0];

  return [0, 0, 0]; // שחור לטקסט רגיל
};

// טיפול ב-Preflight של הדפדפן
export async function OPTIONS() {
  return new NextResponse(null, {
    status: 200,
    headers: corsHeaders,
  });
}

export async function POST(req: Request) {
  try {
    const body = await req.json();
    const { calculatorName, inputs, results } = body;

    const doc = new jsPDF();
    doc.setFontSize(20);
    // הוספתי "TEST" כדי לראות אם זה מתעדכן
    doc.text(`TEST VERSION - WattQuick Report: ${calculatorName}`, 20, 20);
    
    doc.setFontSize(12);
    doc.text("Inputs & Calculation Data:", 20, 40);
    doc.line(20, 42, 190, 42);

    let y = 55;
    Object.entries(inputs || {}).forEach(([key, value]) => {
      doc.text(`${key}: ${String(value)}`, 20, y);
      y += 10;
    });

    y += 10;
    
    // ... אחרי הכותרת Results
    Object.entries(results || {}).forEach(([key, value], index) => {
      const rawVal = Array.isArray(value) ? value[1] : String(value);
      let cleanValue = String(rawVal).replace(/["',]/g, '').trim();
      const cleanKey = String(key).replace(/["',]/g, '').trim();

      // אם הטקסט ארוך מ-45 תווים, נוסיף לו רווח כדי שה-PDF יוכל לרדת שורה
      if (cleanValue.length > 45) {
        cleanValue = cleanValue.replace(/([/\\*])/g, '$1 '); 
      }

      const color = getStatusColor(cleanValue);
      
      // ... (שאר הקוד)
      doc.setTextColor(color[0], color[1], color[2]);
      doc.setFont("helvetica", "normal");
      
      // הדפסה עם הגבלה מחמירה
      doc.text(cleanValue, 110, y, { maxWidth: 70, align: "left" });
      
      doc.setTextColor(0, 0, 0);
      y += 10;
    });

    addFooter(doc);

    const pdfBuffer = Buffer.from(doc.output('arraybuffer'));

    return new NextResponse(pdfBuffer, {
      status: 200,
      headers: {
        'Content-Type': 'application/pdf',
        'Content-Disposition': `attachment; filename="wattquick-report.pdf"`,
        ...corsHeaders, // כאן אנחנו מוודאים שה-Headers תקינים ב-POST
      },
    });
  } catch (error) {
    return NextResponse.json({ error: 'Failed to generate PDF' }, { 
      status: 500,
      headers: corsHeaders 
    });
  }
}